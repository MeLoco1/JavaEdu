public class Employee {
    public String name;
    public String surname;
    public int age;
    public int salary;
    public String sex;

    public Employee(String name, String surname, int age, int salary, String sex) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.salary = salary;
        this.sex = sex;
    }
}
